#include<bits/stdc++.h>
using namespace std;
typedef long long int ll;

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);

    int n, W, H;
    cin >> n >> W >> H;

    for (int i = 1; i <= n ; i++){
        int w, h;
        cin >> w >> h;
        
        if (w <= W || h <= H) continue;

        
    }    
    return 0;
}